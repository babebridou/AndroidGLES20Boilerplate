package com.fairyteller.gles20boilerplate.object;


public class MaterialIndices {
	int materialId;
	int materialOffset;
	int materialLength;
	
	public int getMaterialOffset() {
		return materialOffset;
	}
	public void setMaterialOffset(int materialOffset) {
		this.materialOffset = materialOffset;
	}
	public int getMaterialLength() {
		return materialLength;
	}
	public void setMaterialLength(int materialLength) {
		this.materialLength = materialLength;
	}
	
	public int getMaterialId() {
		return materialId;
	}
	public void setMaterialId(int materialId) {
		this.materialId = materialId;
	}
}
